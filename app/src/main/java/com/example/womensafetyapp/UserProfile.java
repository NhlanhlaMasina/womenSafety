package com.example.womensafetyapp;

public class UserProfile {
    public String Name;
    public String Surname;
    public String Age;
    public String Contact;
    public String Address;
    public String MotherDaughter;


    public UserProfile(String name, String surname, String age, String contact, String address, String motherDaughter) {
        Name = name;
        Surname = surname;
        Age = age;
        Contact = contact;
        Address = address;
        MotherDaughter = motherDaughter;
    }
}
